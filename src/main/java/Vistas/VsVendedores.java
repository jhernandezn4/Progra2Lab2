    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        // TODO add your handling code here:
        String codigo = txtCodigo.getText();
        if (codigo.equals("Nuevo")) {
            vendedor = new VendedoresControlador();
            String nombre = txtNombre.getText();
            Double enero = Double.parseDouble(txtEnero.getText());
            Double febrero = Double.parseDouble(txtFebrero.getText());
            Double marzo = Double.parseDouble(txtMarzo.getText());
            MdVendedor nuevo = vendedor.crear(nombre, enero, febrero, marzo);

            if (nuevo != null) {
                DefaultTableModel modelo = (DefaultTableModel) tblVendedores.getModel();
                modelo.addRow(new Object[]{
                    nuevo.getId(),
                    nuevo.getNombre(),
                    nuevo.getEnero(),
                    nuevo.getFebrero(),
                    nuevo.getMarzo(),
                    nuevo.getPromedio(),
                    nuevo.getTotal()
                });
                nuevoVendedor();
                JOptionPane.showMessageDialog(null, "Vendedor agregado exitosamente");
            }
        }
    }//GEN-LAST:event_btnGuardarMouseClicked

    private void tblVendedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVendedoresMouseClicked
        // TODO add your handling code here:
        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());

        txtCodigo.setText(source.getModel().getValueAt(row, 0).toString());
        txtNombre.setText(source.getModel().getValueAt(row, 1).toString());
        txtEnero.setText(source.getModel().getValueAt(row, 2).toString());
        txtFebrero.setText(source.getModel().getValueAt(row, 3).toString());
        txtMarzo.setText(source.getModel().getValueAt(row, 4).toString());
        btnGuardar.setEnabled(false);
        btnActualizar.setEnabled(true);
        btnEliminar.setEnabled(true);
    }//GEN-LAST:event_tblVendedoresMouseClicked

    private void btnActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarMouseClicked
        // TODO add your handling code here:
        String codigo = txtCodigo.getText();
        if (!codigo.equals("Nuevo")) {
            vendedor = new VendedoresControlador();
            String nombre = txtNombre.getText();
            Double enero = Double.parseDouble(txtEnero.getText());
            Double febrero = Double.parseDouble(txtFebrero.getText());
            Double marzo = Double.parseDouble(txtMarzo.getText());

            MdVendedor actualizado = vendedor.editar(Integer.parseInt(codigo), nombre, enero, febrero, marzo);

            if (actualizado != null) {

                DefaultTableModel modelo = (DefaultTableModel) tblVendedores.getModel();

                for (int i = 0; i < modelo.getRowCount(); i++) {
                    if (modelo.getValueAt(i, 0) == actualizado.getId()) {
                        modelo.setValueAt(actualizado.getId(), i, 0);
                        modelo.setValueAt(actualizado.getNombre(), i, 1);
                        modelo.setValueAt(actualizado.getEnero(), i, 2);
                        modelo.setValueAt(actualizado.getFebrero(), i, 3);
                        modelo.setValueAt(actualizado.getMarzo(), i, 4);
                        modelo.setValueAt(actualizado.getPromedio(), i, 5);
                        modelo.setValueAt(actualizado.getTotal(), i, 6);
                    }

                }
                nuevoVendedor();
                JOptionPane.showMessageDialog(null, "Vendedor actualizado exitosamente");
            }
        }
    }//GEN-LAST:event_btnActualizarMouseClicked

    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        // TODO add your handling code here:
        String codigo = txtCodigo.getText();
        if (!codigo.equals("Nuevo")) {
            vendedor = new VendedoresControlador();
            String nombre = txtNombre.getText();

            Integer eliminado = vendedor.eliminar(nombre);

            if (eliminado == 1) {

                DefaultTableModel modelo = (DefaultTableModel) tblVendedores.getModel();

                for (int i = 0; i < modelo.getRowCount(); i++) {
                    if (Integer.parseInt(modelo.getValueAt(i, 0).toString())==Integer.parseInt(codigo)) {
                        modelo.removeRow(i);
                    }

                }
                nuevoVendedor();
                JOptionPane.showMessageDialog(null, "Vendedor eliminado exitosamente");
            }
        }
    }//GEN-LAST:event_btnEliminarMouseClicked

