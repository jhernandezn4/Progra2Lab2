    public void CargarVendedores() {
        vendedor = new VendedoresControlador();
        List<MdVendedor> lista = new ArrayList<MdVendedor>();
        lista = vendedor.todos();

        for (MdVendedor nuevo : lista) {
            DefaultTableModel modelo = (DefaultTableModel) tblVendedores.getModel();
            modelo.addRow(new Object[]{
                nuevo.getId(),
                nuevo.getNombre(),
                nuevo.getEnero(),
                nuevo.getFebrero(),
                nuevo.getMarzo(),
                nuevo.getPromedio(),
                nuevo.getTotal()
            });
        }

    }

