    private void btnActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarMouseClicked
        // TODO add your handling code here:
        String codigo = txtCodigo.getText();
        if (!codigo.equals("Nuevo")) {
            usuario = new UsuariosControlador();
            String usuarioNuevo = txtUsuario.getText();
            String password = txtPassword.getText();

            MdUsuario actualizado = usuario.editar(Integer.parseInt(codigo), usuarioNuevo, password);

            if (actualizado != null) {

                DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();

                for (int i = 0; i < modelo.getRowCount(); i++) {
                    if (modelo.getValueAt(i, 0) == actualizado.getId()) {
                        modelo.setValueAt(actualizado.getId(), i, 0);
                        modelo.setValueAt(actualizado.getUsuario(), i, 1);
                        modelo.setValueAt(actualizado.getPassword(), i, 2);
                    }

                }
                NuevoUsuario();
                JOptionPane.showMessageDialog(null, "Usuario actualizado exitosamente");
            }
        }
    }//GEN-LAST:event_btnActualizarMouseClicked

