    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        // TODO add your handling code here:
        String codigo = txtCodigo.getText();
        if (codigo.equals("Nuevo")) {
            usuario = new UsuariosControlador();
            String nuevoUsuario = txtUsuario.getText();
            String password = txtPassword.getText();
            MdUsuario nuevo = usuario.crear(nuevoUsuario,password);
            if (nuevo != null) {
                DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
                modelo.addRow(new Object[]{
                    nuevo.getId(),
                    nuevo.getUsuario(),
                    nuevo.getPassword()
                });
                NuevoUsuario();
                JOptionPane.showMessageDialog(null, "Vendedor agregado exitosamente");
            }
        }
    }//GEN-LAST:event_btnGuardarMouseClicked

